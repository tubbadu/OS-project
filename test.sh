echo '

#!/bin/sh

BASE_ADDR=0x090c0000
STATUS_ID=0x0
IN_START_ID=0x8
IN_END_ID=0x400
OUT_START_ID=0x408
OUT_END_ID=0x800

# List of 128 complex values (real and imaginary parts)
INPUT_VALUES=$(cat val.txt)

# Write input values to memory
ADDR=$(($BASE_ADDR + $IN_START_ID))

cat val.txt | while read REAL IMAG; do
    echo "val1 = $REAL, val2=$IMAG"
    busybox devmem $ADDR 64 $REAL
    ADDR=$(($ADDR + 8))
    busybox devmem $ADDR 64 $IMAG
    ADDR=$(($ADDR + 8))
done

# Start FFT operation
busybox devmem $(($BASE_ADDR + $STATUS_ID)) 32 0x1

# Wait for status to be 0x6
while [ "$(busybox devmem $(($BASE_ADDR + $STATUS_ID)) 32)" != "0x00000006" ]; do
    echo "status: $(busybox devmem $(($BASE_ADDR + $STATUS_ID)) 32)"
    sleep 0.1
done

# Read output values from memory
ADDR=$(($BASE_ADDR + $OUT_START_ID))
i=0
while [ $i -lt 256 ]; do
    REAL=$(busybox devmem $ADDR 64)
    ADDR=$(($ADDR + 8))
    IMAG=$(busybox devmem $ADDR 64)
    ADDR=$(($ADDR + 8))
    echo "Output[$((i / 2))]: $REAL + $IMAG j"
    i=$((i + 2))
done

' > test.sh; clear; sh test.sh



